// Basics
let PlusMinusPythonBasics = document.getElementById("PlusMinusPythonBasics")
PlusMinusPythonBasics.addEventListener("click", function () {
    let Change = this.firstElementChild.firstElementChild.firstElementChild;

    if (Change.classList.contains("bi-caret-right-fill")) {
        Change.classList.remove("bi-caret-right-fill")
        Change.classList.add("bi-caret-up-fill")
    } else {
        Change.classList.remove("bi-caret-up-fill")
        Change.classList.add("bi-caret-right-fill")
    }
});
